<template>
  <BaseLinkContainer
    class="item main-simple-list-item"
    :class="{
      disabled: isDeleted
    }"
    :link="link"
    @click="handleLinkClick"
  >
    <BaseDeletedBlock
      v-if="isDeleted"
      model="playlist"
    />
    <template v-else>
      <BaseImage
        class="rounded bordered"
        model="playlist"
        :image="imageData?.extrasmall"
      />

      <div class="content">
        <BaseHeader
          tag="h4"
          :class="{
            link: isMainLinkActive
          }"
          :text="playlistTitle"
        />

        <BasePrivateSection
          v-if="isPrivate"
          class="description"
          model="playlist"
        />

        <span
          v-if="isWithProfileNickname"
          class="description profile-nickname"
          @mouseenter="handleProfileNicknameMouseEnter"
          @mouseleave="handleProfileNicknameMouseLeave"
        >
          <BaseLink
            :text="profileNickname"
            :link="profileMainLink"
            @click="handleLinkClick"
          />
        </span>

        <small
          class="description"
          v-html="tracksCountText"
        />
      </div>

      <BasePlaylistOptionsDropdown
        :share-data="shareData"
        :is-with-share-option="isWithShareOption"
        :is-with-delete-option="isWithDeleteOption && isSelf"
        @delete-option-click="handleDeleteOptionClick"
      />

      <BaseClearButton
        v-if="isWithClearButton"
        @click="handleClearButtonClick"
      />
    </template>
  </BaseLinkContainer>

  <BasePlaylistDeleteModal
    ref="deleteModal"
    :playlist-data="playlistData"
    @success="handleDeleted"
  />
</template>

<script>
import BaseLinkContainer
  from '@/components/containers/links/BaseLinkContainer.vue'
import BaseDeletedBlock from '@/components/BaseDeletedBlock.vue'
import BaseImage from '@/components/images/BaseImage.vue'
import BaseHeader from '@/components/BaseHeader.vue'
import BasePrivateSection from '@/components/BasePrivateSection.vue'
import BaseLink from '@/components/links/BaseLink.vue'
import BasePlaylistOptionsDropdown
  from '@/components/dropdowns/playlist/BasePlaylistOptionsDropdown.vue'
import BaseClearButton from '@/components/buttons/BaseClearButton.vue'
import BasePlaylistDeleteModal
  from '@/components/modals/playlist/BasePlaylistDeleteModal.vue'
import {
  main as formatProfileMainLink,
  playlist as formatProfilePlaylistLink
} from '@/helpers/formatters/links/profile'
import {
  number as formatNumber
} from '@/helpers/formatters'
import {
  isCurrentProfile
} from '@/helpers/utils'
import {
  playlist as formatPlaylistShareData
} from '@/helpers/formatters/share'

export default {
  name: 'PlaylistItem',
  components: {
    BaseLinkContainer,
    BaseDeletedBlock,
    BaseImage,
    BaseHeader,
    BasePrivateSection,
    BaseLink,
    BasePlaylistOptionsDropdown,
    BaseClearButton,
    BasePlaylistDeleteModal
  },
  inject: {
    findPaginationItem: {
      default: () => false
    }
  },
  props: {
    playlistData: {
      type: Object,
      required: true
    },
    isWithProfileNickname: Boolean,
    isWithShareOption: Boolean,
    isWithDeleteOption: Boolean,
    isWithClearButton: Boolean
  },
  emits: [
    'linkClick',
    'clearButtonClick'
  ],
  data () {
    return {
      isMainLinkActive: true
    }
  },
  computed: {
    link () {
      if (this.isMainLinkActive) {
        return this.profilePlaylistLink
      } else {
        return this.profileMainLink
      }
    },
    profilePlaylistLink () {
      return formatProfilePlaylistLink(
        {
          profileId: this.profileId,
          playlistId: this.playlistId
        }
      )
    },
    profileId () {
      return this.profileData.id.toString()
    },
    profileData () {
      return this.playlistData.profile
    },
    profileMainLink () {
      return formatProfileMainLink(
        {
          profileId: this.profileId
        }
      )
    },
    playlistId () {
      return this.playlistData.id.toString()
    },
    imageData () {
      return this.playlistData.image
    },
    playlistTitle () {
      return this.playlistData.title
    },
    tracksCountText () {
      return this.$tc(
        'counters.nominative.tracks',
        this.tracksCount,
        {
          count: this.tracksCountStrong
        }
      )
    },
    tracksCountStrong () {
      return `<strong>${this.tracksCountFormatted}</strong>`
    },
    tracksCountFormatted () {
      return formatNumber(
        this.tracksCount
      )
    },
    tracksCount () {
      return this.playlistData.tracks_count
    },
    isDeleted () {
      return !!this.playlistData.isDeleted
    },
    paginationItem () {
      return this.findPaginationItem(
        {
          uuid: this.uuid
        }
      )
    },
    uuid () {
      return this.playlistData.uuid
    },
    isSelf () {
      return isCurrentProfile(
        this.profileId
      )
    },
    profileNickname () {
      return this.profileData.nickname
    },
    shareData () {
      return formatPlaylistShareData(
        this.playlistData
      )
    },
    isPrivate () {
      return this.playlistData.private
    }
  },
  methods: {
    handleLinkClick () {
      this.$emit(
        'linkClick'
      )
    },
    handleDeleteOptionClick () {
      this.showDeleteModal()
    },
    handleClearButtonClick () {
      this.$emit(
        'clearButtonClick',
        {
          uuid: this.uuid
        }
      )
    },
    handleDeleted () {
      this.paginationItem
        .isDeleted = true
    },
    handleProfileNicknameMouseEnter () {
      this.isMainLinkActive = false
    },
    handleProfileNicknameMouseLeave () {
      this.isMainLinkActive = true
    },
    showDeleteModal () {
      this.$refs
        .deleteModal
        .show()
    }
  }
}
</script>

<style lang="sass" scoped>
