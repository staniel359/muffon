<template>
  <BaseModalContainer
    ref="modal"
    @visible="handleVisible"
  >
    <SearchContent
      ref="search"
    />
  </BaseModalContainer>
</template>

<script>
import {
  mapActions
} from 'vuex'
import BaseModalContainer
  from '@/components/containers/modals/BaseModalContainer.vue'
import SearchContent from './TheSearchModal/SearchContent.vue'

export default {
  name: 'TheSearchModal',
  components: {
    BaseModalContainer,
    SearchContent
  },
  provide () {
    return {
      hideSearch: this.hide
    }
  },
  mounted () {
    this.setSearchModal(
      this.$refs.modal
    )
  },
  methods: {
    ...mapActions(
      'layout',
      [
        'setSearchModal'
      ]
    ),
    handleVisible () {
      this.focusInput()
    },
    show () {
      this.$refs
        .modal
        .show()
    },
    hide () {
      this.$refs
        .modal
        .hide()
    },
    focusInput () {
      this.$refs
        .search
        .focusInput()
    }
  }
}
</script>

<style lang="sass" scoped>
.main-modal
  top: calc((100vh - #{$mainModalHeight}) / 2)
</style>
