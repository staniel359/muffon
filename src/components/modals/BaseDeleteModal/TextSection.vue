<template>
  <div class="content">
    <p
      class="main-text-section"
      v-html="goingTextSanitized"
    />

    <template
      v-if="isWithAlsoText"
    >
      <template
        v-if="isAlsoTextArray"
      >
        <p
          v-for="(alsoTextItem, index) in alsoText"
          :key="index"
          class="main-text-section"
          v-html="alsoTextItem"
        />
      </template>
      <p
        v-else
        class="main-text-section"
        v-text="alsoText"
      />
    </template>

    <p
      class="main-text-section"
    >
      <strong
        v-text="undoText"
      />
    </p>

    <p
      class="main-text-section"
      v-text="sureText"
    />
  </div>
</template>

<script>
import {
  sanitizeString
} from '@/helpers/utils'

export default {
  name: 'TextSection',
  props: {
    modelType: {
      type: String,
      required: true
    },
    model: String,
    modelName: String,
    parentModelName: String,
    isWithAlsoText: Boolean
  },
  computed: {
    goingTextSanitized () {
      return sanitizeString(
        this.goingText
      )
    },
    goingText () {
      return this.$t(
        `modals.delete.going.${this.modelType}`,
        {
          modelName: this.modelNameStrong,
          parentModelName: this.parentModelNameStrong
        }
      )
    },
    modelNameStrong () {
      return `<strong>${this.modelName}</strong>`
    },
    parentModelNameStrong () {
      return `<strong>${this.parentModelName}</strong>`
    },
    isAlsoTextArray () {
      return this.alsoText instanceof Array
    },
    alsoText () {
      return this.$tm(
        [
          'modals.delete.also',
          this.modelType,
          this.model
        ].filter(
          e => e
        ).join(
          '.'
        )
      )
    },
    undoText () {
      return this.$t(
        'modals.delete.undo'
      )
    },
    sureText () {
      return this.$t(
        'modals.delete.sure'
      )
    }
  }
}
</script>

