<template>
  <BaseSegmentContainer>
    <BaseListContainer>
      <div
        :class="[
          'item main-simple-list-item',
          'main-playlist-info-item'
        ]"
      >
        <div class="playlist-image-container">
          <BaseZoomableImage
            model="playlist"
            size="small"
            :image-data="imageData"
          />
        </div>

        <div class="content">
          <BaseHeader
            tag="h3"
            :text="playlistTitle"
          />

          <BasePrivateSection
            v-if="isPrivate"
            class="description"
            model="playlist"
          />

          <div
            class="description"
            v-html="tracksCountText"
          />

          <BaseTimestamp
            class="description"
            :created="created"
            is-only-date
          />
        </div>

        <BasePlaylistOptionsDropdown
          class="playlist-options"
          :share-data="shareData"
          :is-with-edit-option="isSelf"
          :is-with-delete-option="isSelf"
          is-with-share-option
          @edit-option-click="handleEditOptionClick"
          @delete-option-click="handleDeleteOptionClick"
        />
      </div>
    </BaseListContainer>
  </BaseSegmentContainer>

  <BasePlaylistUpdateModal
    ref="editModal"
    :playlist-data="playlistData"
  />

  <BasePlaylistDeleteModal
    ref="deleteModal"
    :playlist-data="playlistData"
    is-delete-with-redirect
  />
</template>

<script>
import BaseSegmentContainer
  from '@/components/containers/segments/BaseSegmentContainer.vue'
import BaseListContainer
  from '@/components/containers/lists/BaseListContainer.vue'
import BaseZoomableImage from '@/components/images/BaseZoomableImage.vue'
import BaseHeader from '@/components/BaseHeader.vue'
import BasePrivateSection from '@/components/BasePrivateSection.vue'
import BaseTimestamp from '@/components/BaseTimestamp.vue'
import BasePlaylistOptionsDropdown
  from '@/components/dropdowns/playlist/BasePlaylistOptionsDropdown.vue'
import BasePlaylistUpdateModal
  from '@/components/modals/playlist/BasePlaylistUpdateModal.vue'
import BasePlaylistDeleteModal
  from '@/components/modals/playlist/BasePlaylistDeleteModal.vue'
import {
  number as formatNumber
} from '@/helpers/formatters'
import {
  isCurrentProfile
} from '@/helpers/utils'
import {
  playlist as formatPlaylistShareData
} from '@/helpers/formatters/share'

export default {
  name: 'InfoSegment',
  components: {
    BaseSegmentContainer,
    BaseListContainer,
    BaseZoomableImage,
    BaseHeader,
    BasePrivateSection,
    BaseTimestamp,
    BasePlaylistOptionsDropdown,
    BasePlaylistUpdateModal,
    BasePlaylistDeleteModal
  },
  props: {
    playlistData: {
      type: Object,
      required: true
    },
    profileId: {
      type: String,
      required: true
    }
  },
  computed: {
    imageData () {
      return this.playlistData.image
    },
    playlistTitle () {
      return this.playlistData.title
    },
    tracksCountText () {
      return this.$tc(
        'counters.nominative.tracks',
        this.tracksCount,
        {
          count: this.tracksCountStrong
        }
      )
    },
    tracksCountStrong () {
      return `<strong>${this.tracksCountFormatted}</strong>`
    },
    tracksCountFormatted () {
      return formatNumber(
        this.tracksCount
      )
    },
    tracksCount () {
      return this.playlistData.tracks_count
    },
    created () {
      return this.playlistData.created
    },
    isSelf () {
      return isCurrentProfile(
        this.profileId
      )
    },
    shareData () {
      return formatPlaylistShareData(
        this.playlistData
      )
    },
    isPrivate () {
      return this.playlistData.private
    }
  },
  methods: {
    handleEditOptionClick () {
      this.showEditModal()
    },
    handleDeleteOptionClick () {
      this.showDeleteModal()
    },
    showEditModal () {
      this.$refs
        .editModal
        .show()
    },
    showDeleteModal () {
      this.$refs
        .deleteModal
        .show()
    }
  }
}
</script>

