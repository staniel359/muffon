<template>
  <VideoItem
    v-for="(videoData, index) in videosCollection"
    :key="videoData.uuid"
    :video-data="videoData"
    :index="index"
  />
</template>

<script>
import VideoItem from './VideosList/VideoItem.vue'
import {
  collection as formatCollection
} from '@/helpers/formatters'

export default {
  name: 'VideosList',
  components: {
    VideoItem
  },
  props: {
    videos: {
      type: Array,
      default () {
        return []
      }
    }
  },
  computed: {
    videosCollection () {
      return formatCollection(
        this.videos
      )
    }
  }
}
</script>

